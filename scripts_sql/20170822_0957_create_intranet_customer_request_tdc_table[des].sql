    MONTINCOME VARCHAR(100), -- INGRESOS MENSUALES
    OTHEINCOME VARCHAR(100), -- INGRESOS OTROS

    --REFERENCIAS PERSONALES
    REF1NAMES VARCHAR(100), -- NOMBRE Y APELLIDOS DE REFERENCIAS
    REF1PHORES VARCHAR(100), -- TELEFONO RESIDENCIAL
    REF1PHOCEL VARCHAR(100), -- TELEFONO CELULAR

    REF2NAMES VARCHAR(100), -- NOMBRE Y APELLIDOS DE REFERENCIAS
    REF2PHORES VARCHAR(100), -- TELEFONO RESIDENCIAL
    REF2PHOCEL VARCHAR(100), -- TELEFONO CELULAR

    CREATED_BY VARCHAR(45),
    CREATENAME VARCHAR(100),
    CREATED_AT TIMESTAMP,

    UPDATED_BY VARCHAR(45),
    UPDATENAME VARCHAR(100),
