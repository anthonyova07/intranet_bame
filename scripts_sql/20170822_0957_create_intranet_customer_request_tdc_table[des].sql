    LAST_NAMES VARCHAR(150),
    IDENTIFICA VARCHAR(100),
    BIRTDATE DATE,
    NATIONALIT VARCHAR(50),
    GENDER CHAR(1),
    MARISTATUS CHAR(1),

    PSTREET VARCHAR(150),
    PNUM VARCHAR(10),
    PBUILDING VARCHAR(100),
    PAPARTAMEN VARCHAR(100),
    PSECTOR VARCHAR(100),
    PCOUNTRY VARCHAR(100),
    PMAIL VARCHAR(100),
    PNEAR VARCHAR(100), --CERCA DE
